package b_condicionais;

public class Condicionais3Encadeada {

    public static void main(String[] args) {

        if (3 == 2) {
            System.out.println("3 é igual ao 2");
        } else if (3 < 2) {
            System.out.println("3 é menor que 2");
        } else if (3 < 1) {
            System.out.println("3 é menor que 1");
        } else {
            System.out.println("3 é o maior");
        }

    }
}
